double readShockSensor(double sensorLengthInInches) {
	int num = 10;
	double adcAverage;
	int adcSamples[num];

	for(int i=0;i<num;i++){
		//start the ADC
		HAL_ADC_Start(&hadc1);
		//get value from ADC
		//HAL_OK is a predefined constant in the HAL library, indicating that the ADC conversion was successful
		if(HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY) == HAL_OK){
			adcSamples[i] = HAL_ADC_GetValue(&hadc1);
		}
		HAL_ADC_Stop(&hadc1);
		HAL_Delay(10);
	}

	for(int i=0;i<num;i++){
		adcAverage += adcSamples[i];
	}
	adcAverage /= num;
	return getDistanceInches(adcAverage, sensorLengthInInches);
}

double getDistanceInches(double voltageIn, double sensorLengthInInches) {
	double adcRange = 4096; // 12-bit ADC
	int degree = 7; // degree of interpolating polynomial
	double extraTravel = 0.125; // additional mechanical travel of the sensor in inches
	double interpolatingPolynomial[8] = {0.010338598, 1.73038646, -7.095112602, 39.63360702, -107.2815134, 150.2003369, -106.2700875, 30.07762097};
	double voltageInScaled = 0;
	// Horner's method to calculate the polynomial
	for (int i = degree; i >=0; --i) {
		voltageInScaled = voltageInScaled*(voltageIn/adcRange) + interpolatingPolynomial[i];
	}
	return voltageInScaled*(sensorLengthInInches + extraTravel);
}

